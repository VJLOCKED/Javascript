// ORDER OF OPERATIONS

// * comes before + and - 
var numb1 = 10 + 10 * 10 - 10;

// operations in parenthesis have highest priority
var numb2 = (10 + 10) * 10 - 10;

// operations occur left to right in order of operator precedence
var numb3 = (10 + 10) * (10 - 10);

console.log(numb1, numb2, numb3);

// MODULUS OPERATOR

